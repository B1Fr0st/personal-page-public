// Scans src/assets/covers/ and regenerates src/data/book-covers.js with a
// static import for each cover image. Wired to `predev` and `prebuild` so
// adding a new cover only requires dropping a file in src/assets/covers/
// named after the book's `slug` (e.g. `dune.jpg` matches `slug: 'dune'`).

import { readdir, writeFile } from 'node:fs/promises';
import { join, dirname, basename, extname } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const coversDir = join(here, '..', 'src', 'assets', 'covers');
const outFile = join(here, '..', 'src', 'data', 'book-covers.js');

const IMAGE_EXTS = new Set(['.jpg', '.jpeg', '.png', '.webp', '.avif', '.gif']);

function toIdent(filename) {
  const base = basename(filename, extname(filename));
  return (
    'cover_' +
    base
      .split(/[^a-zA-Z0-9]+/)
      .filter(Boolean)
      .map((part, i) =>
        i === 0
          ? part[0].toLowerCase() + part.slice(1)
          : part[0].toUpperCase() + part.slice(1)
      )
      .join('')
  );
}

const entries = (await readdir(coversDir).catch((err) => {
  if (err.code === 'ENOENT') return [];
  throw err;
}))
  .filter((f) => IMAGE_EXTS.has(extname(f).toLowerCase()))
  .sort();

const imports = entries
  .map((f) => `import ${toIdent(f)} from '../assets/covers/${f}';`)
  .join('\n');

const mapEntries = entries
  .map((f) => `  '${basename(f, extname(f))}': ${toIdent(f)}`)
  .join(',\n');

const contents = `// AUTO-GENERATED by scripts/generate-book-covers.js. Do not edit by hand.
// Drop a cover in src/assets/covers/ named after the book's slug.
${imports}

export const bookCovers = {
${mapEntries}
};
`;

await writeFile(outFile, contents);
console.log(`Generated ${outFile} with ${entries.length} cover(s)`);
